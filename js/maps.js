// Convert 2D arrays to 1D for easier access
function flattenMap(map2d) {
    const flat = [];
    for (let y = 0; y < map2d.length; y++) {
        for (let x = 0; x < map2d[y].length; x++) {
            flat.push(map2d[y][x]);
        }
    }
    return flat;
}
